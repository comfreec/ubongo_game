import 'dart:io';
import 'verify_puzzles.dart';

// 조각 조합을 바꿔서 통과하는 퍼즐 찾기
// 14칸 보드 패턴들 (이미 통과한 것들)
// m06: L4+S4+Z4+I2 OK ['#####','#####','.####']
// m09: L4+O4+I4+I2 OK ['#####','#####','####.']

// 15칸 보드 패턴들 (이미 통과한 것들)
// m10: T4+L4+S4+I3 OK ['#####','#####','#####']
// m16: T4+L4+O4+I3 OK ['######','######','###']
// m17: T4+L4+I4+I3 OK ['###','######','######']
// m18: L4+O4+I4+I3 OK ['######','###','######']
// m22: L4+S4+O4+I3 OK ['#####','#####','#####']
// m23: L4+Z4+O4+I3 OK ['#####','#####','#####']

// 아직 못 찾은 조합들:
// m05: T4+S4+Z4+I2 = 14칸
// m08: T4+O4+I4+I2 = 14칸
// m14: T4+S4+Z4+I3 = 15칸
// m15: L4+S4+Z4+I3 = 15칸
// m21: T4+S4+Z4+I3 = 15칸 (m14와 같은 조합 - 다른 보드)

// 전략: 조각 조합을 바꿔서 새로운 퍼즐로 대체
// 14칸 보드 패턴 후보들
final boardPatterns14 = [
  ['.###.','#####','#####'],  // 14칸
  ['#####','.####','#####'],  // 14칸
  ['#####','####.','#####'],  // 14칸
  ['.####','#####','#####'],  // 14칸
  ['####.','#####','#####'],  // 14칸
  ['#####','#####','.####'],  // 14칸
  ['#####','#####','####.'],  // 14칸
];

// 15칸 보드 패턴 후보들
final boardPatterns15 = [
  ['#####','#####','#####'],  // 15칸
  ['#####','#####','####.'],  // 14칸 - 아님
  ['######','######','###'],  // 15칸
  ['###','######','######'],  // 15칸
  ['######','###','######'],  // 15칸
  ['######','######','.###.'],  // 15칸
  ['######','######','###..'],  // 15칸
  ['######','######','..###'],  // 15칸
];

// 14칸 조각 조합들 (아직 안 쓴 것들)
final pieceCombos14 = [
  ['T4','S4','Z4','I2'],  // m05 실패
  ['T4','O4','I4','I2'],  // m08 실패
  ['S4','Z4','O4','I2'],  // 새 조합
  ['T4','Z4','O4','I2'],  // 새 조합
  ['T4','S4','O4','I2'],  // 새 조합
  ['L4','Z4','I4','I2'],  // 새 조합
  ['S4','Z4','I4','I2'],  // 새 조합
  ['T4','Z4','I4','I2'],  // 새 조합
];

// 15칸 조각 조합들
final pieceCombos15 = [
  ['T4','S4','Z4','I3'],  // m14, m21 실패
  ['L4','S4','Z4','I3'],  // m15 실패
  ['S4','Z4','O4','I3'],  // 새 조합
  ['T4','Z4','O4','I3'],  // 새 조합
  ['T4','S4','O4','I3'],  // 새 조합
  ['L4','Z4','O4','I3'],  // 새 조합
  ['S4','Z4','I4','I3'],  // 새 조합
  ['T4','Z4','I4','I3'],  // 새 조합
  ['L4','Z4','I4','I3'],  // 새 조합
  ['T4','S4','I4','I3'],  // 새 조합
  ['L4','S4','I4','I3'],  // 새 조합
];

void main() {
  print('=== 14칸 조합 탐색 ===');
  for (final combo in pieceCombos14) {
    for (final pattern in boardPatterns14) {
      final p = fromPattern('test', pattern, combo);
      if (hasSolution(p)) {
        print('OK: ${combo.join('+')} | ${pattern.join(',')}');
        break;
      }
    }
  }
  
  print('\n=== 15칸 조합 탐색 ===');
  for (final combo in pieceCombos15) {
    bool found = false;
    for (final pattern in boardPatterns15) {
      final p = fromPattern('test', pattern, combo);
      if (hasSolution(p)) {
        print('OK: ${combo.join('+')} | ${pattern.join(',')}');
        found = true;
        break;
      }
    }
    if (!found) print('FAIL: ${combo.join('+')}');
  }
}
