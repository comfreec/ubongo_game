import 'dart:io';
import 'verify_puzzles.dart';

void main() {
  // e01은 ['####','##..','###.'] + T4+I2+I3 OK
  // e02는 다른 패턴으로 T4+I2+I3 = 9칸
  final patterns = [
    ['####','.###','##..'],
    ['####','..##','.###'],
    ['####','###.','..##'],
    ['.###','####','##..'],
    ['##..','####','.###'],
    ['###.','####','..##'],
    ['##..','####','###.'],
    ['.###','####','##..'],
    ['#.','##','##','##'],
    ['.#','##','##','##'],
    ['##','##','##','#.'],
    ['##','##','##','.#'],
    ['###','###','###'],
    ['#####','####.'],
    ['.####','#####'],
    ['####.','#####'],
    ['#####','.####'],
    ['##.','###','####'],
    ['####','###','##.'],
    ['####','###','.##'],
    ['.##','###','####'],
    ['##.','####','###'],
    ['.##','####','###'],
    ['###','####','##.'],
    ['###','####','.##'],
  ];
  
  // e01 패턴 (중복 제외)
  final e01Pattern = '####,##..,###.';
  
  for (final pat in patterns) {
    final p = fromPattern('e02', pat, ['T4','I2','I3']);
    final patStr = pat.join(',');
    if (patStr == e01Pattern) continue;
    if (hasSolution(p)) {
      print('e02 OK: $patStr');
    }
  }
}
