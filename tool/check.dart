  void check(List<List<String>> list, String label, int target) {
    print('\n=== $label (목표 ${target}칸) ===');
    for (final p in list) {
      final cnt = p.fold(0, (s,r) => s + r.split('').where((c)=>c=='#').length);
      if (cnt == target) print('✓ $cnt  ${p.join(" | ")}');
    }
