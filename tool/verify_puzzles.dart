// 퍼즐 검증 스크립트: 보드 칸 수 vs 조각 합계 확인
void main() {
  final pieceSize = {
    'I2': 2, 'I3': 3, 'L3': 3, 'I4': 4, 'O4': 4,
    'T4': 4, 'L4': 4, 'S4': 4, 'Z4': 4,
    'L5': 5, 'T5': 5, 'P5': 5,
  };

  final puzzles = [
    ('p01', ['#..','##.','.##','..#','..#','.##'], ['T4','L3','I2']),
    ('p02', ['.##','##.','#..','##.','##.'], ['S4','L3','I2']),
    ('p03', ['#..','#..','##.','.#.','.#.','.##','..#'], ['L4','L3','I2']),
    ('p04', ['###','.#.','##.','#..','##.'], ['T4','I3','I2']),
    ('p05', ['#..','#..','##.','.##','..#','..#','.##'], ['L4','S4','I2']),
    ('p06', ['###.','##..','.##.','..##','...#','...#','..##'], ['T4','L4','L3','I2']),
    ('p07', ['###.','.#..','.##.','##..','#...','##..','.##.','..#.'], ['T4','L4','S4','I2']),
    ('p08', ['##..','##..','.##.','##..','#...','##..','.##.'], ['O4','S4','L3','I2']),
    ('p09', ['#...','##..','.##.','..##','...#','..##','.##.','##..'], ['L4','Z4','T4','I2']),
    ('p10', ['##...','##...','#....','###..','..#..','..##.','...#.','...##'], ['P5','T4','L3','I2']),
    ('p11', ['#...','#...','#...','##..','.##.','..##','...#','..##','.##.'], ['L5','S4','L3','I2']),
    ('p12', ['.#..','###.','.#..','.##.','##..','#...','##..','.##.','..#.'], ['T5','L4','Z4','I2']),
  ];

  bool allOk = true;
  for (final (id, pattern, pieces) in puzzles) {
    final boardCells = pattern.fold(0, (sum, row) => sum + row.split('').where((c) => c == '#').length);
    final pieceCells = pieces.fold(0, (sum, p) => sum + (pieceSize[p] ?? 0));
    final ok = boardCells == pieceCells;
    if (!ok) allOk = false;
    print('${ok ? "✓" : "✗"} $id: board=$boardCells pieces=$pieceCells (${pieces.join("+")})');
  }
  print(allOk ? '\n모두 OK!' : '\n❌ 불일치 있음');
}
