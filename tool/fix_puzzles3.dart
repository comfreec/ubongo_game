void main() {
  final groups = <String, List<Puzzle>>{};
  for (final p in candidates) {
    final base = p.id.replaceAll(RegExp(r'_r\d+$'), '');
    groups.putIfAbsent(base, () => []).add(p);
  }
  
  for (final entry in groups.entries) {
    bool found = false;
    for (final p in entry.value) {
      if (hasSolution(p)) {
        print('${entry.key} -> ${p.id}: OK (pattern: ${_patternStr(p)})');
        found = true;
        break;
      }
    }
    if (!found) print('${entry.key}: NO SOLUTION FOUND');
  }
}

String _patternStr(Puzzle p) {
  return p.grid.map((row) => row.map((c) => c ? '#' : '.').join()).join(',');
}
