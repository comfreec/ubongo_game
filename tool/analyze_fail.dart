void main() {
  final pieceSizes = {'I2':2,'I3':3,'L3':3,'I4':4,'O4':4,'T4':4,'L4':4,'S4':4,'Z4':4,'L5':5,'T5':5,'P5':5};
  
  final puzzles = <String, (List<String>, List<String>)>{
    'e01': (['###.','####','..##'], ['T4','I2','I3']),
    'e02': (['..##','####','###.'], ['T4','I2','I3']),
    'm01': (['#####','#####','####'], ['T4','L4','S4','I2']),
    'm02': (['####','#####','#####'], ['T4','L4','S4','I2']),
    'm04': (['.####','#####','#####'], ['T4','L4','O4','I2']),
    'm05': (['#####','.####','#####'], ['T4','S4','Z4','I2']),
    'm07': (['####.','#####','#####'], ['T4','L4','I4','I2']),
    'm08': (['#####','####.','#####'], ['T4','O4','I4','I2']),
    'm12': (['######','######','###'], ['T4','L4','S4','I3']),
    'm13': (['###','######','######'], ['T4','L4','Z4','I3']),
    'm14': (['######','###','######'], ['T4','S4','Z4','I3']),
    'm15': (['######','######','###'], ['L4','S4','Z4','I3']),
    'm19': (['######','######','###'], ['T4','O4','I4','I3']),
    'm20': (['#####','#####','#####'], ['T4','L4','O4','I3']),
    'm21': (['#####','#####','#####'], ['T4','S4','Z4','I3']),
  };
  
  for (final e in puzzles.entries) {
    final pattern = e.value.$1;
    final pieces = e.value.$2;
    final boardCells = pattern.fold(0, (s, r) => s + r.split('').where((c) => c == '#').length);
    final pieceCells = pieces.fold(0, (s, p) => s + pieceSizes[p]!);
    print('${e.key}: board=$boardCells pieces=$pieceCells (${pieces.join('+')}) ${boardCells == pieceCells ? "SIZE_OK" : "SIZE_MISMATCH"}');
  }
}
