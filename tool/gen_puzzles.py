  ('e11', ['##.','####','###'], ['L4','L3','I2']),
  ('e12', ['###','####','.##'], ['L4','L3','I2']),
  # T4+L3+I2=4+3+2=9
  ('e13', ['.##','####','###'], ['T4','L3','I2']),
  ('e14', ['###','####','##.'], ['T4','L3','I2']),
  # I4+L3+I2=9
  ('e15', ['#..','####','####'], ['I4','L3','I2']),   # 1+4+4=9 ok
  ('e16', ['####','####','..#'], ['I4','L3','I2']),
