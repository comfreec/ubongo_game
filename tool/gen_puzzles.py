  ('e02', ['..##','####','###.'], ['T4','I2','I3']),
  ('e03', ['#...','####','####'], ['I4','I3','I2']),  # 1+4+4=9 ok
  ('e04', ['####','####','#...'], ['I4','I3','I2']),
  ('e05', ['##..','####','..##'], ['O4','I3','I2']),  # 2+4+2=8 no
  # O4+I3+I2=4+3+2=9
  ('e05', ['##.','####','###'], ['O4','I3','I2']),    # 2+4+3=9 ok
  ('e06', ['###','####','##.'], ['O4','I3','I2']),
