"""
추가 퍼즐 패턴 생성 및 칸 수 검증
쉬움(9칸): 3조각, 보통(14~15칸): 4조각, 어려움(20~21칸): 5~6조각
"""

pieces = {
  'I2':2,'I3':3,'L3':3,'I4':4,'O4':4,'T4':4,'L4':4,'S4':4,'Z4':4,
  'L5':5,'T5':5,'P5':5
}

def count(p): return sum(r.count('#') for r in p)
def check(pat, ps):
    c = count(pat)
    n = sum(pieces[p] for p in ps)
    return c == n

# ── 쉬움 추가 (9칸, 3조각) ──────────────────────────────────────
# 현재 10개 → 30개 목표 (20개 추가)
